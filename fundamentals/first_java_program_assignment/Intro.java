public class Intro {
//String[] represents an array of strings, args is the name of the array which can be anything
    public static void main(String[] args) { 
        System.out.println("My name is Mitchell Esparza");
        System.out.println("I am 9+10 years old");
        System.out.println("My hometown is Burbank, CA");
        
        //---------CONDITIONALS------------
        //creating a ternary operator to make our code more concise
        boolean isRaining= true;
        System.out.println(isRaining ? "Wear a raincoat!" : "Sun's out, guns out!");
    }
}
